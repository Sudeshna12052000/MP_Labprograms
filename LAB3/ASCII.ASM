;TO PRINT ASCII VALUE OF INPUT
.MODEL SMALL
.DATA
MSG1 DB 0DH,0AH,"ENTER ALPHANUMERIC CHARACTER : $"
RES DB 02H DUP(0)

.CODE
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1
CALL DISP

MOV AH,01H  ;READING INPUT CHARACTER FROM YOUR KEYBOARD
INT 21H

MOV BL,AL   ;0A IS THE INPUT WILL BE STORED ALWAYS IN AL AND BL REGISTER
MOV CL,4
SHR AL,CL   ;00H
CMP AL,0AH
JC DIGIT    ;AL<0AH CARRY IS GENERATED SO NOW ONLY ADD 30H
ADD AL,07H

DIGIT:
ADD AL,30H  ;30H
MOV RES,AL
AND BL,0FH  ;BL=0AH
CMP BL,0AH
JC DIGIT1
ADD BL,07H  ;BL=0A+07H

DIGIT1:
ADD BL,30H  ;BL=0A+07+30 = 0A+37 = 41H

MOV AH,00H  ;CLEAR THE SCREEN
MOV AL,03H
INT 10H

MOV AH,02H  ;SET THE CURSOR POSITION
MOV BH,00H
MOV DH,0CH  ;ROW NUMBER
MOV DL,28H  ;COLUMN NUMBER

MOV RES+2,'$'  ;$ HERE INDICATES END OF THE RESULT STRING
LEA DX,RES

CALL DISP   ;RES VALUE WILL BE PRINTED ON THE SCREEN
MOV AH,4CH
INT 21H

DISP PROC NEAR
MOV AH,09H
INT 21H
RET
DISP ENDP

END
